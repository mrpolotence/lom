package com.company;


public abstract class Flower {
    private double price;
    private String color;
    private String name;

    public Flower(double price, String color, String name) {
        this.price = price;
        this.color = color;
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public double getColor() {
        return price;
    }

    public double getName() {
        return price;
    }

}